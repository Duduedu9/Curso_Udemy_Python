#Tipos Int e float
#int -> numéro inteiro
# O tipo int representa qualquer numero
# O positivo ou negativo. Int sem sinal é considerado
# positivo.
print (11) #int
print(-11) #int
print(0) #int
# float -> Numero com ponto flutuante
# O tipo de float representa qualquer numero
# positivo ou negativo como ponto flutuante.
# float sem sinal é considerado positivo.
print(1.1, 10.11)
print(0.0, -1.5)
#A função type mostra o tipo que o Python 
# inferiu ao valor.
print(type('Eduardo'))
print(type(1))
print(type(1.1))
print(type(1.1), type(-1.1), type (0.0))